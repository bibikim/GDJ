package practice03;

public class BreadAndChange {

	// 필드
	private int bread;    //
	private int change;
	
	
	// 생성자
	public BreadAndChange() {	// 매개변수 x
	}
	
	
	public BreadAndChange(int bread, int change) {	// 매개변수 o
		super();
		this.bread = bread;
		this.change = change;
	}


	public int getBread() {
		return bread;
	}
	public void setBread(int bread) {
		this.bread = bread;
	}
	public int getChange() {
		return change;
	}
	public void setChange(int change) {
		this.change = change;
	}
	
	
	
	
	
	
}
